package Practice;

public class PrimeNumber {
	
	public static void main(String[] args) {
		
		int num = 7, temp=0;
		System.out.println(num);
		{
			for(int i = 2; i<num-1; i ++) {
				
				temp = temp+1;	
			}
			
			
		} if(temp>0) 
		{
		
		System.out.println("number is prime");
		}else {
			System.out.println("number is not prime");
		}
		
		
	} 

}
